/**
 * 面向对象编程：用对象的思想去写代码
 * 我们一直都在使用对象：数组Array，时间Date
 * 我们把系统自带的对象叫做系统对象，但是系统对象满足不了我们的需求，
 * 面向对象（OOP）的特点：
 * 抽象：抓住核心问题
 * 封装：只能通过对象来访问方法
 * 继承：从已有对象上继承出新的对象
 * 多态：多对象的不同形态
 * 
 * 对象的组成：
 * 方法（行为，操作）：---函数：过程，动态的
 * 属性：---变量：状态，静态的
 * 对象下面的变量：叫做对象的属性
 * 对象下面的函数：叫做对象的方法
 * 
 * 当new去调用一个函数时：这时函数中的this就是创建出来的对象，而且函数的返回值直接就是this（隐式返回，不写return的话默认返回this）
 * new后面调用的函数：叫做构造函数
 */